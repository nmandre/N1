package gan.javastruct;

public class Friend 
{
	public void m1()
	{
		System.out.println("Neha");
	}
	public void m2()
	{
		System.out.println("Priya");
	}
	public static void main(String[]args)
	{
		System.out.println("She is the best");
		Friend f = new Friend();
		f.m1();
		System.out.println("She is clever");
		f.m2();
		
	}
}
